    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed

        // 1. Validar los datos
        String nombre = jTextFieldNombre.getText();
        String anio = jTextFieldAnio.getText();

        if (nombre.isEmpty() || anio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // No continÃºes si los datos no son vÃ¡lidos
        }

        // 2. Validar el estado (booleano)
        if (!estado) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un estado vÃ¡lido.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // No continÃºes si el estado no es vÃ¡lido
        }

        // 3. Crear un objeto Materia con los datos ingresados
        Materia nuevaMateria = new Materia(Integer.parseInt(jTextIdVista.getText()), anio, estado);

        // public Materia(int idMateria, int anioMateria, String nombre, boolean estado)
        // 4. Guardar la Materia en tu fuente de datos (por ejemplo, en MateriaData)
        MateriaData materiaData = new MateriaData();
        materiaData.guardarMateria(nuevaMateria); // Invoca el mÃ©todo para guardar

        // 5. Actualizar la interfaz de usuario si es necesario (por ejemplo, limpiar campos o mostrar un mensaje de Ã©xito)
        // Restablecer los campos
        jTextFieldNombre.setText("");
        jTextFieldAnio.setText("");
        jRadioButtonEstado.setSelected(false); // Reinicia el estado a "false" si se usa un JRadioButton

        // Actualizar el prÃ³ximo ID (puedes hacerlo segÃºn tu estructura de datos)
        int ultimoId = nuevaMateria.getIdMateria();
        jTextIdVista.setText(String.valueOf(ultimoId + 1));

        // Mostrar un mensaje de Ã©xito
        JOptionPane.showMessageDialog(this, "Materia guardada con Ã©xito.", "Ãxito", JOptionPane.INFORMATION_MESSAGE);


    }//GEN-LAST:event_jButtonGuardarActionPerformed


